#include <stdio.h>

int main()
{
    int i = 10; // Intitialize i with 10
    int j = i;
    int a = 2, b = 3, c = 4, d = 5; // Initialize j with i;
    printf("The value of i is %d and j is %d\n", i, j);
    printf("The value of a is %d and b is %d\n", a, b); // %d is called PLaceholder or format specifier
    printf("The value of c is %d and d is %d\n", c, d);
    /* %d is for int
       %f is for float
       %ld is for long
       %lf is for double */

    return 0; // return statement
}